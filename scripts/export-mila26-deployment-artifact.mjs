/* global console */

import { createHash } from 'node:crypto';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const sourceArtifactPath = resolve(
  repoRoot,
  'artifacts/contracts/Mila26RestrictedFundToken.sol/Mila26RestrictedFundToken.json',
);
const outputPath = resolve(repoRoot, 'src/contracts/mila26RestrictedFundTokenDeploymentArtifact.ts');

const sourceArtifact = JSON.parse(readFileSync(sourceArtifactPath, 'utf8'));
const bytecode = sourceArtifact.bytecode;

if (sourceArtifact.contractName !== 'Mila26RestrictedFundToken') {
  throw new Error(`Unexpected contract artifact: ${sourceArtifact.contractName}`);
}

if (typeof bytecode !== 'string' || !bytecode.startsWith('0x') || bytecode.length <= 2) {
  throw new Error('Compiled Mila26RestrictedFundToken bytecode is missing or invalid.');
}

const bytecodeHash = `sha256-${createHash('sha256').update(bytecode).digest('hex')}`;
const relativeSourcePath = relative(repoRoot, sourceArtifactPath);

const moduleSource = `// Generated by npm run contracts:export-artifact after npm run contracts:build.
// Source: ${relativeSourcePath}
// Do not edit bytecode by hand; refresh from the compiled Hardhat artifact.
import type { Abi, Hex } from 'viem';

export const mila26RestrictedFundTokenDeploymentArtifact = {
  contractName: ${JSON.stringify(sourceArtifact.contractName)},
  sourceName: ${JSON.stringify(sourceArtifact.sourceName)},
  artifactFormat: ${JSON.stringify(sourceArtifact._format)},
  artifactSource: 'hardhat_compiled_artifact',
  compileCommand: 'npm run contracts:build -- --force',
  exportCommand: 'npm run contracts:export-artifact',
  compileTestCommand: 'npm run test:contracts',
  abi: ${JSON.stringify(sourceArtifact.abi, null, 2)} as const satisfies Abi,
  bytecode: ${JSON.stringify(bytecode)} as Hex,
  bytecodeHash: ${JSON.stringify(bytecodeHash)},
} as const;

export type Mila26RestrictedFundTokenDeploymentArtifact = typeof mila26RestrictedFundTokenDeploymentArtifact;
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, moduleSource);
console.log(`Exported ${relative(repoRoot, outputPath)} (${bytecodeHash})`);
